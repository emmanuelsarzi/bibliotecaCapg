package clases;

import java.util.Date;

public class Autor {
	String nombre;
	String nacionalidad;
	Date fechaDeNacimiento;
	public Autor(String nombre, String nacionalidad, Date fechaDeNacimiento) {
		super();
		this.nombre = nombre;
		this.nacionalidad = nacionalidad;
		this.fechaDeNacimiento = fechaDeNacimiento;
	}
}
